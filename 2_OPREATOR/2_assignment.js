// Assignment Operators Example

// Initializing a variable
let total = 100;

// Simple Assignment (=)
console.log("Initial Value:", total); 
// Add and Assign (+=)
total += 50; 
console.log("After += 50:", total); 
// Subtract and Assign (-=)
total -= 30; 
console.log("After -= 30:", total); 
// Multiply and Assign (*=)
total *= 2;
console.log("After *= 2:", total); 

// Divide and Assign (/=)
total /= 4; // total = total / 
console.log("After /= 4:", total);

// Modulus and Assign (%=)
total %= 7; 
console.log("After %= 7:", total);

// Exponentiation and Assign (**=)
total **= 3;
console.log("After **= 3:", total);

// Bitwise AND and Assign (&=)
total &= 3; 
console.log("After &= 3:", total); 

// Bitwise OR and Assign (|=)
total |= 5; 
console.log("After |= 5:", total);

// Bitwise XOR and Assign (^=)
total ^= 2;
console.log("After ^= 2:", total);

// Left Shift and Assign (<<=)
total <<= 2; 
console.log("After <<= 2:", total); 

// Right Shift and Assign (>>=)
total >>= 1; 
console.log("After >>= 1:", total); 

// Unsigned Right Shift and Assign (>>>=)
total >>>= 1; 
console.log("After >>>= 1:", total);
